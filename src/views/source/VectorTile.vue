import { ref, reactive, computed } from 'vue'
import { Folder, FolderOpened, Operation, Link, Document } from '@element-plus/icons-vue'

const activeNames = ref([])
const isAllExpanded = ref(false)

const toggleExpand = () => {
  isAllExpanded.value = !isAllExpanded.value
  activeNames.value = isAllExpanded.value ? apiData.map(api => api.name) : []
}

const currentCategory = ref('all')
const categories = reactive([
  { name: 'all', cn: '全部', count: 0 },
  { name: 'constructor', cn: '构造函数', count: 0 }
])

const filterByCategory = (category) => {
  currentCategory.value = category
}

const apiData = [
  {
    name: 'VectorTileSource',
    cn: '构造函数',
    type: 'constructor',
    category: 'constructor',
    description: '创建一个新的矢量切片数据源。矢量切片数据源用于加载 MVT/PBF 等格式的矢量切片数据。',
    params: [
      {
        name: 'options',
        type: 'Object',
        default: '{}',
        description: '配置选项对象',
        children: [
          { name: 'attributions', type: 'AttributionLike', default: undefined, description: '归属信息' },
          { name: 'attributionsCollapsible', type: 'boolean', default: 'true', description: '归属信息是否可折叠' },
          { name: 'cacheSize', type: 'number', default: undefined, description: '初始切片缓存大小' },
          { name: 'extent', type: 'Extent', default: undefined, description: '范围' },
