          { name: 'maxZoom', type: 'number', default: '22', description: '最大缩放级别' },
          { name: 'minZoom', type: 'number', default: undefined, description: '最小缩放级别' },
          { name: 'tileSize', type: 'number|Size', default: '512', description: '切片大小' },
          { name: 'maxResolution', type: 'number', default: undefined, description: '零级别切片网格分辨率' },
          { name: 'tileGrid', type: 'TileGrid', default: undefined, description: '切片网格' },
          { name: 'tileLoadFunction', type: 'LoadFunction', default: undefined, description: '加载切片的函数' },
          { name: 'tileUrlFunction', type: 'UrlFunction', default: undefined, description: '获取切片 URL 的函数' },
