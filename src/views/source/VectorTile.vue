          { name: 'wrapX', type: 'boolean', default: 'true', description: '是否水平环绕世界' },
          { name: 'zDirection', type: 'number|Function', default: '1', description: '整数缩放级别间的切片选择' }
        ]
      }
    ],
    usage: `// ============================================
// VectorTileSource 构造函数使用示例
// ============================================

import VectorTileSource from 'ol/source/VectorTile'
import MVT from 'ol/format/MVT'

// 【示例 1】基础用法 - 加载 MVT 切片
const vectorTileSource = new VectorTileSource({
  format: new MVT(),
  url: 'https://example.com/tiles/{z}/{x}/{y}.pbf',
  maxZoom: 14
})

// 【示例 2】使用多个 URL（负载均衡）
const vectorTileSource = new VectorTileSource({
  format: new MVT(),
  urls: [
    'https://a.example.com/tiles/{z}/{x}/{y}.pbf',
    'https://b.example.com/tiles/{z}/{x}/{y}.pbf'
  ]
})

// 【示例 3】Mapbox 矢量切片
const vectorTileSource = new VectorTileSource({
  format: new MVT(),
  url: 'https://api.mapbox.com/v4/mapbox.streets/{z}/{x}/{y}.vector.pbf?access_token=YOUR_TOKEN',
  maxZoom: 14
})

// 【示例 4】MapTiler 矢量切片
const vectorTileSource = new VectorTileSource({
  format: new MVT(),
