<script setup>
import { ref, computed } from 'vue'

const activeNames = ref(['constructor'])
const currentCategory = ref('all')
const isAllExpanded = ref(false)

const toggleExpand = () => {
  isAllExpanded.value = !isAllExpanded.value
  activeNames.value = isAllExpanded.value ? wmtsCapabilitiesApis.map(api => api.name) : []
}

const filterByCategory = (category) => {
  currentCategory.value = category
}

const getTagType = (type) => {
  const typeMap = {
    'constructor': 'primary',
    'method': 'success'
  }
  return typeMap[type] || 'info'
}

const categories = computed(() => {
  const cats = [
    { name: 'all', cn: '全部', count: wmtsCapabilitiesApis.length },
    { name: 'constructor', cn: '构造函数', count: wmtsCapabilitiesApis.filter(a => a.type === 'constructor').length },
    { name: 'methods', cn: '方法', count: wmtsCapabilitiesApis.filter(a => a.type === 'method').length }
  ]
  return cats.filter(c => c.count > 0 || c.name === 'all')
})

const filteredApis = computed(() => {
  if (currentCategory.value === 'all') return wmtsCapabilitiesApis
  if (currentCategory.value === 'constructor') return wmtsCapabilitiesApis.filter(a => a.type === 'constructor')
  if (currentCategory.value === 'methods') return wmtsCapabilitiesApis.filter(a => a.type === 'method')
  return wmtsCapabilitiesApis
})

const wmtsCapabilitiesApis = [
  {
    name: 'constructor',
    cn: '构造函数',
    type: 'constructor',
    category: 'constructor',
    description: '创建一个新的 WMTSCapabilities 格式实例。WMTSCapabilities 格式用于读取 WMTS capabilities 文档，解析 WMTS 服务器提供的图层和瓦片矩阵信息。',
    usage: `import WMTSCapabilities from 'ol/format/WMTSCapabilities.js';

// 创建 WMTSCapabilities 格式实例
const wmtsCapabilitiesFormat = new WMTSCapabilities();

// 从 WMTS GetCapabilities 响应读取
fetch('https://example.com/wmts?service=WMTS&request=GetCapabilities')
  .then(response => response.text())
  .then(xml => {
    const capabilities = wmtsCapabilitiesFormat.read(xml);
    console.log(capabilities);
  });`,
    params: [],
    returns: { type: 'WMTSCapabilities', description: '新的 WMTSCapabilities 格式实例' }
  },
  {
    name: 'read',
    cn: '读取 Capabilities',
    type: 'method',
    category: 'methods',
    description: '从 WMTS GetCapabilities 响应中读取 capabilities 对象。解析 XML 并返回包含服务信息、图层列表、瓦片矩阵集等的 JavaScript 对象。',
    usage: `import WMTSCapabilities from 'ol/format/WMTSCapabilities.js';
import { optionsFromCapabilities } from 'ol/source/WMTS.js';

const format = new WMTSCapabilities();

// 读取 WMTS Capabilities
fetch('https://example.com/wmts?service=WMTS&request=GetCapabilities')
  .then(response => response.text())
  .then(xml => {
    const capabilities = format.read(xml);

    // 访问服务信息
    console.log('服务名称:', capabilities.ServiceIdentification.Title);

    // 访问图层列表
    const layers = capabilities.Contents.Layer;
    layers.forEach(layer => {
      console.log('图层:', layer.Identifier, layer.Title);
    });

    // 访问瓦片矩阵集
    const tileMatrixSets = capabilities.Contents.TileMatrixSet;
    console.log('瓦片矩阵集:', tileMatrixSets);

    // 创建 WMTS 源配置
    const wmtsOptions = optionsFromCapabilities(capabilities, {
      layer: 'layerName',
      matrixSet: 'EPSG:3857'
    });
  });`,
    params: [
      { name: 'source', type: 'Document|Node|string', default: '', description: 'WMTS GetCapabilities 响应 XML 文档或字符串' }
    ],
    returns: { type: 'Object', description: 'WMTS capabilities 对象' }
  },
  {
    name: 'readProjection',
    cn: '读取投影信息',
    type: 'method',
    category: 'methods',
    description: '从 WMTS capabilities 中读取投影/坐标系信息。',
    usage: `import WMTSCapabilities from 'ol/format/WMTSCapabilities.js';

const format = new WMTSCapabilities();

// 读取支持的投影
const capabilities = format.read(capabilitiesXml);
const projection = format.readProjection(capabilities);

console.log(projection.getCode());`,
    params: [
      { name: 'source', type: 'Document|Node|string', default: '', description: 'WMTS capabilities XML 文档或字符串' }
    ],
    returns: { type: 'Projection', description: '投影对象' }
  }
]
</script>

